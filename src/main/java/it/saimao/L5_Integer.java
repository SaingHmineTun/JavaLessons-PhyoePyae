package it.saimao;
/*
Integer data types
byte (1 byte)
short (2 bytes)
int (4 bytes)
long (8 bytes)
 */
public class L5_Integer {
    public static void main(String[] args) {
        byte b = 127; // 1 byte
        short s = 32767; // 2 bytes
        int i = -2_147_483_648; // 4 bytes ( DEFAULT )
        long l = -2_147_483_649L; // 8 bytes
    }
}
/*
 1KB => 1024 bytes
 1MB => 1024 KB
 1GB => 1024 MB
 1TB => 1024 GB
 */
