package it.saimao;

public class L12_AssignmentOperator {
    public static void main(String[] args) {

        byte b = 10;
        System.out.println(b += 7); // b = b + 7;
        System.out.println(b -= 5);
        System.out.println(b *= 5);
        System.out.println(b /= 10);
        System.out.println(b %= 3);

    }
}
